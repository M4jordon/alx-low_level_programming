#include "main.h"

/**
 * add - adds numbers
 * @a: first integer
 * @b: second integer
 * Return: result
 */

int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - subtracts numbers
 * @a: first integer
 * @b: second integer
 * Return: result
 */

int sub( int a, int b)
{
	return (a - b);
}

/**
 * mul - multiply
 * @a: first integer
 * @b: second integer
 * Return: result
 */

int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - divides numbers
 * @a: first integer
 * @b: second integer
 * Return: result
 */

int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - modulus of numbers
 * @a: first integer
 * @b: second integer
 * Return: result
 */

int mod(int a, int b)
{
	return (a % b);
}

